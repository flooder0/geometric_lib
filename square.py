def area(a):
    return a * a

# Параметры:
# a (float): Длина стороны квадрата.
# Возвращает:
# float: Площадь квадрата, которая равна a * a.

# Пример использования:
# s = area(4)
# print(s)  # Вывод: 16.0


def perimeter(a):
    return 4 * a

# Параметры:
# a (float): Длина стороны квадрата.
# Возвращает:
# float: Периметр квадрата, который равен 4 * a.

# Пример использования:
# p = perimeter(4)
